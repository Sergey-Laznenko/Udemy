//? Обявления переменных

// 1. var - ключевое слово для объявления переменных. Сейчас не рекомендуется использовать. Устарело
// --- ECMAScript 6 ---
// 2. let - позволяет создать переменную, значение можно присвоить или изменить пожже
// 3. const - позволяет создать переменную, значение присваивается сразу при создании переменной, дальнейшее изменение значения НЕ ВОЗМОЖНО 



// Примеры 2

