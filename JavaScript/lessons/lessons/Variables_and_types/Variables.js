// 1. PascalCase - используется в названиях типов и классов
// 2. DB_PASSWORD - используется в том случае, кога значения переменных известны до запуска(изначально) приложения и НЕ меняются, если таких переменных много, они часто пишутся в отдельном файле и просто экспортируются оттуда
// 3. camelCase - используется во всех остальных случаях

